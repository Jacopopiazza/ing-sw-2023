package it.polimi.ingsw.Model;

/**
 * The TileColor enum represents the different colors of tiles in the game.
 */
public enum TileColor {

    /**
     * White color tile.
     */
    WHITE,

    /**
     * Fuchsia color tile.
     */
    FUCHSIA,

    /**
     * Blue color tile.
     */
    BLUE,

    /**
     * Cyan color tile.
     */
    CYAN,

    /**
     * Yellow color tile.
     */
    YELLOW,

    /**
     * Green color tile.
     */
    GREEN
}
